#ifndef STATE_H
#define STATE_H

template <typename entry_type>
class State
{

public:
	/**Constructor*/
	State() = default;

	/**Destructor*/
	virtual ~State() =default;

	/***/
	virtual void Enter(entry_type*) = 0;

	/**実行関数*/
	virtual void Excute(entry_type*) = 0;

	/***/
	virtual void Exit(entry_type*) = 0;

};

#endif	// STATE_H

